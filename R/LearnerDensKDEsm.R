    train_internal = function(task){

      pars = self$param_set$get_values(tag="train")

      data = as.numeric(unlist(task$data(cols = task$target_names)))

      saved_ctrl = sm::sm.options()
      on.exit(invoke(sm::sm.options, .args = saved_ctrl))
      sm::sm.options(add = TRUE, display = "none", delta = NA, eval.grid = FALSE, panel = FALSE,
                     positive = FALSE, rugplot = FALSE)
      is_ctrl_pars = (names(pars) %in% names(saved_ctrl))

      # ensure only relevant pars passed to fitted model
      if (any(is_ctrl_pars)) {
        do.call(sm::sm.options, pars[is_ctrl_pars])
        pars = pars[!is_ctrl_pars]
      }


      pdf <- function(x1){}

      body(pdf) <- substitute({

        invoke(sm::sm.density, x = data, eval.points = x1, .args = pars)$estimate

      })


      Distribution$new(name = "sm KDE Gaussian",
                       short_name = "smKDEGaus",
                       pdf = pdf)
    },

    predict_internal = function(task){

      newdata = as.numeric(unlist(task$data(cols = task$target_names)))

      PredictionDens$new(task = task, pdf = self$model$pdf(newdata))

    }
